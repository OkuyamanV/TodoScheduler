/* ============================
   CelebrationEngine: Web Audio API の基礎エンジンと音階マッピング
   目的: ブラウザのオーディオ機能の初期化と、人間が読める「音符（ドレミ）」を
         機械が発音できる「周波数（Hz）」に変換する辞書を提供する
   ============================ */
const CelebrationEngine = {
    // --------------------------------------------------------
    // 1. AudioContext の初期化
    // --------------------------------------------------------
    // Web Audio API の心臓部。すべての音はこのコンテキスト上で生成・接続・出力される。
    // ※ Safariなどの一部ブラウザ向けに webkitAudioContext にもフォールバック対応
    ctx: new (window.AudioContext || window.webkitAudioContext)(),

    // --------------------------------------------------------
    // 2. 音階と周波数(Hz)の変換辞書
    // --------------------------------------------------------
    // 記号ルール: 
    //   ↓↓ = 2オクターブ下 (ベース音など)
    //   ↓  = 1オクターブ下
    //   無印 = 標準オクターブ (真ん中のドを中心とした音域)
    //   ↑  = 1オクターブ上
    noteMap: {
        'ソ↓↓': 98.00, 'ラ↓↓': 110.00, 'シ↓↓': 123.47,
        'ド↓': 130.81, 'レ↓': 146.83, 'ミ↓': 164.81, 'ファ↓': 174.61, 'ソ↓': 196.00, 'ラ↓': 220.00, 'シ↓': 246.94,
        'ド': 261.63, 'レ': 293.66, 'ミ': 329.63, 'ファ': 349.23, 'ソ': 392.00, 'ラ': 440.00, 'シ': 493.88,
        'ド↑': 523.25, 'レ↑': 587.33, 'ミ↑': 659.25, 'ファ↑': 698.46, 'ソ↑': 783.99
    },

    // --------------------------------------------------------
    // 3. ユーティリティメソッド
    // --------------------------------------------------------
    
    // 音符名（例: 'ド↑'）を周波数（数値）に変換する
    // ※ 辞書にない文字列（'休' など）が渡された場合は 0 を返す。
    // SoundManager側で「0 = 音を鳴らさない（休符）」として扱われる。
    noteToFreq(noteName) {
        return this.noteMap[noteName] || 0;
    },
};